a = "Vandana is a good girl\nbut not a bad girl"  #New line

print(a)

b = "Naksh is a good boy\nbut not a bad\t boy" #Tab

print(b)

c = "Naksh is a good boy\nbut not a bad\t \"boy\""  #Double quote

print(c)

d = "Hello\bWorld"  # \b means backspace
print(d)

e = ("Hello\rWorld")  # \r means carriage return
print(e)
